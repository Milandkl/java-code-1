//Here is an program of demostrate continue//
class main{
    public static void main(String args[]){
        for(int i=1;i<=10;++i){
            if(i>4&&i<9)
            {
                continue;
            }
            System.out.println(i);
        }
    }
}
/* 
output:
1
2
3
4
9
10
Explanation:
I created a class main
Then,I created a for loop
I created a continue statement.
 */